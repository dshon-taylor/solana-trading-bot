    fmtCt,
    effectiveWindowStartMs,
    updatedIso,
    elapsedHours,
    warmingUp,
    scansPerHourWindow,
    watchlistSize,
    hotDepth,
    evalsPerMinute,
    providerRate,
    providers,
    providerBirdEyeRateWin,
    providerJupiterRateWin,
    providerDexRateWin,
    scannerHealth,
    uniqueCandidatesSeen,
    uniqueAboveHotFloor,
    routeable,
    pairFetchRate,
    preHotConsidered,
    preHotPassed,
    preHotFailed,
    preHotConsideredWin,
    preHotPassedWin,
    preHotFailedWin,
    hotEnq,
    hotCons,
    hotEnqWin,
    hotConsWin,
    cumulativeMomentumEvaluated,
    cumulativeConfirmReached,
    cumulativeAttempt,
    cumulativeFill,
    primaryChoke,
    chokeStage,
    top5Blockers,
    preHotMinLiqActive,
    state,
    cfg,
    hotStalkingFloor,
    bypassAllowed,
    bypassRejected,
