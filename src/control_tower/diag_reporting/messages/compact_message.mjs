export function buildCompactDiagMessage(ctx) {
  const {
    windowHeaderLabel,
    fmtCt,
    effectiveWindowStartMs,
    updatedIso,
    elapsedHours,
    warmingUp,
    scansPerHourWindow,
    watchlistSize,
    hotDepth,
    evalsPerMinute,
    providerRate,
    providers,
    scannerHealth,
    uniqueCandidatesSeen,
    uniqueAboveHotFloor,
    routeable,
    pairFetchRate,
    preHotConsidered,
    preHotPassed,
    preHotFailed,
    preHotConsideredWin,
    preHotPassedWin,
    preHotFailedWin,
    hotEnq,
    hotCons,
    hotEnqWin,
    hotConsWin,
    cumulativeMomentumEvaluated,
    cumulativeConfirmReached,
    cumulativeAttempt,
    cumulativeFill,
    primaryChoke,
    chokeStage,
    top5Blockers,
    preHotMinLiqActive,
    state,
    cfg,
    hotStalkingFloor,
    bypassAllowed,
    bypassRejected,
    bypassPrimaryReject,
    bypassAllowedWin,
    bypassRejectedWin,
    bypassPrimaryRejectWin,
    stalkableCandidates,
    stalkableBand,
    liqBandFromMomentum,
    liqBand,
    downstreamShort,
    examples,
  } = ctx;

  return [
    `🧪 *Diag (compact)* window=${windowHeaderLabel} start=${fmtCt(effectiveWindowStartMs)}`,
    'SYSTEM',
    `snapshotAt=${updatedIso} windowHours=${elapsedHours.toFixed(2)} scansPerHour=${warmingUp ? 'warming_up' : scansPerHourWindow.toFixed(1)} watchlistSize=${watchlistSize} hotDepth=${hotDepth} evalsPerMinute=${warmingUp ? 'warming_up' : evalsPerMinute.toFixed(2)} status=${warmingUp ? 'warming_up' : 'ready'}`,
    `providers(lifetime): birdeye=${providerRate(providers?.birdeye)} jupiter=${providerRate(providers?.jupiter)} dex=${providerRate(providers?.dexscreener)}`, // window unavailable for provider counters
    `scannerSummary: scansPerHour=${warmingUp ? 'warming_up' : scansPerHourWindow.toFixed(1)} scannerHealth=${scannerHealth} uniqueCandidatesSeen=${uniqueCandidatesSeen} uniqueCandidatesAboveHotFloor=${uniqueAboveHotFloor}`,
    '',
    'PIPELINE HEALTH',
    `routeable=${routeable} pairFetchOkRate=${pairFetchRate} preHot considered/passed/failed=${preHotConsideredWin}/${preHotPassedWin}/${preHotFailedWin} hot enqueued/consumed=${hotEnqWin}/${hotConsWin} momentumEvaluated=${cumulativeMomentumEvaluated} confirmReached=${cumulativeConfirmReached} attemptReached=${cumulativeAttempt} fill=${cumulativeFill}`, // all bounded to requested window
    '',
    'PRIMARY CHOKE',
    `primaryChoke=${primaryChoke} chokeStage=${chokeStage}`,
    ...(top5Blockers.length ? top5Blockers : ['- none']),
    '',
    'CANDIDATE SHAPE',
    `preHotLiquidityThresholdActive=${Math.round(preHotMinLiqActive)} source=${state?.filterOverrides?.MIN_LIQUIDITY_USD != null ? 'filterOverride.MIN_LIQUIDITY_USD' : 'cfg.MIN_LIQUIDITY_FLOOR_USD'} hotStalkingFloor=${Math.round(hotStalkingFloor)} bypassAllowed=${bypassAllowedWin ?? 'n/a'} bypassRejected=${bypassRejectedWin ?? 'n/a'} bypassPrimaryReject=${bypassPrimaryRejectWin || 'n/a'}`,
    `stalkableCandidates=${stalkableCandidates} stalkableBandBreakdown(>=hotFloor): 30–50k=${stalkableBand.b30_50} 50–75k=${stalkableBand.b50_75} 75k+=${stalkableBand.gte75}`,
    `liqBand(${liqBandFromMomentum ? 'momentum' : 'watchlist'}): <30k=${liqBand.lt30} 30–50k=${liqBand.b30_50} 50–75k=${liqBand.b50_75} 75k+=${liqBand.gte75}`,
    '',
    'DOWNSTREAM',
    `momentumRule=mature:3_of_4 early(<30m):2_of_3`,
    downstreamShort,
    '',
    'RECENT EXAMPLES',
    ...(examples.length ? examples : ['- none']),
  ].join('\n');
}
