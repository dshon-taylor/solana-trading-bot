    liqBand,
    funnelByBand,
    topRejectionByBand,
    preMomentumBlockersByBandText,
    hotReasonMissByBand,
    shortlistPreByBand,
    shortlistSelectedByBand,
    shortlistDroppedByBand,
    shortlistDropTopReasonByBand,
    reserveObserved,
    reserveBlocked,
    reserveBlockedWin,
    targetUsdTooSmallLife,
    targetUsdTooSmallWin,
    reserveReasonTop,
    reserveBlockedReasonTop,
